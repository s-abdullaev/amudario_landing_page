// resolve "p:oxusWs.features.cards.0.title" from window.PRODUCTS_I18N
function resolveProductKey(lang, key) {
  const P = window.PRODUCTS_I18N;
  if (!P) return null;
  const root = P[lang] || P.en;
  let cur = root;
  for (const part of key.split(".")) {
    if (cur == null) return null;
    cur = cur[part];
  }
  if (typeof cur !== "string") return null;
  // product tags come numbered ("01 — Agrometeostation") — drop the enumeration
  if (key.endsWith("hero.tag")) cur = cur.replace(/^\s*\d+\s*[—-]\s*/, "");
  return cur;
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (key.startsWith("p:")) {
      const v = resolveProductKey(lang, key.slice(2));
      if (v != null) el.textContent = v;
      return;
    }
    if (dict[key] != null) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-attr]").forEach(el => {
    // format: "placeholder:cta.placeholder"
    el.getAttribute("data-i18n-attr").split(",").forEach(pair => {
      const [attr, key] = pair.split(":");
      if (dict[key] != null) el.setAttribute(attr, dict[key]);
    });
  });
  document.querySelectorAll("[data-lang-btn]").forEach(b => {
    b.classList.toggle("active", b.getAttribute("data-lang-btn") === lang);
  });
  try { localStorage.setItem("amudario_lang", lang); } catch (e) {}
  window.dispatchEvent(new CustomEvent("amudario:lang", { detail: { lang } }));
}

document.addEventListener("DOMContentLoaded", () => {
  let saved = "en";
  try { saved = localStorage.getItem("amudario_lang") || "en"; } catch (e) {}
  applyLang(saved);
  document.querySelectorAll("[data-lang-btn]").forEach(b => {
    b.addEventListener("click", () => applyLang(b.getAttribute("data-lang-btn")));
  });
});
